
public class OperatorMain03 {

	public static void main(String[] args) {
		int a = 3;
		
		a += 5;
		System.out.println("a : " + a);
		
		int b = 3;
		b -= 3;
		System.out.println("a : " + b);
		
		int c = 3;
		c *= 2;
		System.out.println("a : " + c);
		
		int d = 3;
		d /= 5;
		System.out.println("a : " + d);
		
		int e = 3;
		e %= 2;
		System.out.println("a : " + e);
		
	}

}
