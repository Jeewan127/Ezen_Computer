
public class OperatorMain05 {

	public static void main(String[] args) {
		int a = 3;
		int b = 4;
		boolean c;
		
		c = ( a==b );
		System.out.println("c = " + c);
		
		c = ( a!=b );
		System.out.println("c = " + c);
		
		a = 3;
		b = 3;
		
		c = ( a==b );
		System.out.println("c = " + c);
		
		c = ( a!=b );
		System.out.println("c = " + c);
		
	}

}
